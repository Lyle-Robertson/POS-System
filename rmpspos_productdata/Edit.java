        for (int a = 0; a < products.size(); a ++){
            if (oldDesc.equals(((RMPSPOS_ProductData) products.get(a)).getProdDescription())){
                 products.remove(a);
            }
        }
        DefaultTableModel searchTable = (DefaultTableModel)ProductTable2.getModel();
        searchTable.setRowCount(0);
        Desc.setText("");
        InvQty.setText("");
        Cost.setText("");
        Price.setText("");
        supplier.setText("");
        StkQty.setText("");
        
        JFrame frame = new JFrame();
