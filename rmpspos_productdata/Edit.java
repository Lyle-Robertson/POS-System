    private void saveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChangesActionPerformed
        // TODO add your handling code here:
        if (Desc.getText().isBlank() || Cost.getText().isBlank() || Price.getText().isBlank() ||
                InvQty.getText().isBlank() || StkQty.getText().isBlank() || supplier.getText().isBlank()){
            
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "CHECK INPUT");
        }
        else{
            
            String productDescription = Desc.getText();
            int newQty = Integer.parseInt(InvQty.getText());
            int newStock = Integer.parseInt(StkQty.getText());
            String newCost = Cost.getText();
            String newSellingPrice = Price.getText();
            String newSupplierName = supplier.getText();

            for (int i = 0; i < products.size(); i ++){
                if (((RMPSPOS_ProductData) products.get(i)).getProdDescription().equals(oldDesc)){
                    ((RMPSPOS_ProductData) products.get(i)).setProdDescription(productDescription);
                    ((RMPSPOS_ProductData) products.get(i)).setStockQty(newStock);
                    ((RMPSPOS_ProductData) products.get(i)).setCostPrice(Float.parseFloat(newCost));
                    ((RMPSPOS_ProductData) products.get(i)).setPrice(Float.parseFloat(newSellingPrice));
                    ((RMPSPOS_ProductData) products.get(i)).setInventoryQty(newQty);
                    ((RMPSPOS_ProductData) products.get(i)).setSupplier(newSupplierName);
                }
            }

            DefaultTableModel searchTable = (DefaultTableModel)ProductTable2.getModel();

            searchTable.setRowCount(0);
            Desc.setText("");
            InvQty.setText("");
            Cost.setText("");
            Price.setText("");
            supplier.setText("");
            StkQty.setText("");

            JFrame frame = new JFrame();

            JOptionPane.showMessageDialog(frame, "ACTION COMPLETE");

            try {
                    SavingChangesToFile();
                } 
            catch (IOException ex) {
                    Logger.getLogger(Add.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_saveChangesActionPerformed

